/**
 * the same as `Math.max()`
 * @param a   { number }
 * @param b   { number }
 * @returns {*}
 */
export const max = (a, b) => {
  return a > b ? a : b;
};

/**
 * the same as `Math.min()`
 * @param a   { number }
 * @param b   { number }
 * @returns {*}
 */
export const min = (a, b) => {
  return a < b ? a : b;
};
